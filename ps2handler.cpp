void ps2handler::init_recv_buffer()
{
    for(uint8_t i=0; i<recv_buffer_size; ++i)
    {
        recv_buffer[i]=0;
    }
    recv_buffer_head=0;
    recv_buffer_tail=0;
}

uint8_t ps2handler::get_scan_code()
{
    uint8_t result,i;

    i=recv_buffer_tail;
    if(i==recv_buffer_head)
        return 0;
    i++;

    if(i>=recv_buffer_size)
        i=0;

    result=recv_buffer[i];
    recv_buffer_tail=i;
    return result;
}

